value= int(12.547)
print(value)  # Output: 12

value = float(12)
print(value)  # Output: 12.0

value = str(12.547)
print(value)  # Output: '12.547'

value = bool(0)
print(value)  # Output: False

value = bool(1)
print(value)  # Output: True

value = bool("Hello")
print(value)  # Output: True

value = bool("")
print(value)  # Output: False

value = list((1, 2, 3))
print(value)  # Output: [1, 2, 3]

value = tuple([1, 2, 3])
print(value)  # Output: (1, 2, 3)

value = bin(45)
print(value)  # Output: '0b101101'

value = hex(255)
print(value)  # Output: '0xff'

value = oct(64)
print(value)  # Output: '0o100'