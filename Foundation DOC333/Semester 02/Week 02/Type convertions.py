value= int(12.547)
print(value)  # Output: 12

value = float(12)
print(value)  # Output: 12.0

value = str(12.547)
print(value)  # Output: '12.547'

value = bool(0)
print(value)  # Output: False

value = bool(1)
print(value)  # Output: True

value = bool("Hello")
print(value)  # Output: True

value = bool("")
print(value)  # Output: False

value = list((1, 2, 3))
print(value)  # Output: [1, 2, 3]

value = tuple([1, 2, 3])
print(value)  # Output: (1, 2, 3)

